package userbean;

public class userbean 
{
	private int CardNo;
	private int PinNo;
	private int Balance;
	private int card;
	private int pin;
	private int Bal;
	public int getCardNo() {
		return CardNo;
	}
	public void setCardNo(int cardNo) {
		CardNo = cardNo;
	}
	public int getPinNo() {
		return PinNo;
	}
	public void setPinNo(int pinNo) {
		PinNo = pinNo;
	}
	public int getBal() {
		return Bal;
	}
	public void setBal(int bal) {
		Bal = bal;
	}
	public int getCard() {
		return card;
	}
	public void setCard(int card) {
		this.card = card;
	}
	public int getPin() {
		return pin;
	}
	public void setPin(int pin) {
		this.pin = pin;
	}
	public int getBalance() {
		return Balance;
	}
	public void setBalance(int balance) {
		this.Balance = balance;
	}
	

}
